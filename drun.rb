class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v0.6.1.tar.gz"
  version "0.6.1"
  sha256 "64f3947f05d0dcd9a0bc2eda30d7f6d1efb21bce5676de82b8695046fe66e5cf"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
