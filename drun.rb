class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v2.1.0.tar.gz"
  version "2.1.0"
  sha256 "ea4c431faee8354562dfcd4d0ee7dec692c45732b2bae5b70620dc26776ba76c"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
