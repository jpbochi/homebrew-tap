class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v1.2.1.tar.gz"
  version "1.2.1"
  sha256 "385fe338eb68977f11fb7de4874c6feb4c1a806728de6976492b6f92cb26f35d"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
