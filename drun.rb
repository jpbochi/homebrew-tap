class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v0.5.0.tar.gz"
  version "0.5.0"
  sha256 "c79ed7f68826e664a385e13b6f2f341893b3403cad8c80c8d1076745d401ceeb"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
