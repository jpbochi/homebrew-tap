class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v0.7.1.tar.gz"
  version "0.7.1"
  sha256 "79fa4d995a2add0d9be0ce73767000d1923fa6551e4d096942c8c70f447a4a5f"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
