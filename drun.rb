class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v0.3.1.tar.gz"
  version "0.3.1"
  sha256 "6881f86def713b46a766bde40ec1de1df89d3dffd572d6179bcc943e4cb1f1f0"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
