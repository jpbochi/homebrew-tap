class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v0.6.0.tar.gz"
  version "0.6.0"
  sha256 "eb350f34e955579679948eabf6ff1e695036495d27ad0649ae64f80cb05cc17d"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
