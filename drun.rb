class Drun < Formula
  desc "A docker run that automatically mounts your current directory"
  homepage "https://github.com/jpbochi/drun"
  url "https://github.com/jpbochi/drun/archive/v1.1.0.tar.gz"
  version "1.1.0"
  sha256 "e413332b9a129b71a09d5ea437543c7902da7ba436fb80023b9b963c7c355446"
  # to get new sha256, do `wget --no-verbose -O - https://... | shasum -a 256`

  def install
    bin.install "drun"
  end

  test do
    system "#{bin}/drun alpine true"
  end
end
